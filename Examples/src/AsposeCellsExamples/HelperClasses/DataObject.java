package AsposeCellsExamples.HelperClasses;
// ExStart: 1
public class DataObject
{
    private int id;
    private String name;

    public DataObject(int id, String name)
    {
        this.id = id;
        this.name = name;
    }

    public int getId()
    {
        return this.id;
    }

    public void setId(int value)
    {
        this.id = value;
    }

    public String getName()
    {
        return this.name;
    }

    public void setName(String value)
    {
        this.name = value;
    }
}
