package com.aspose.cells.examples.SmartMarkers;

public class Wife {
	private String m_Name;
	private int m_Age;

	public Wife(String name, int age) {
		this.m_Name = name;
		this.m_Age = age;
	}

	public String getName() {
		return m_Name;
	}

	public int getAge() {
		return m_Age;
	}

}
